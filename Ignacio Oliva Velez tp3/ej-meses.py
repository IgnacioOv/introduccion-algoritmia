def main():
    # ingreso del numero de mes

    num = int(input("Ingrese un numero "))

    # imprimo en pantalla el mes

    if num == 1:
        print("Enero")
    elif num == 2:
        print("Febrero")
    elif num == 3:
        print("Marzo")
    elif num == 4:
        print("Abril")
    elif num == 5:
        print("Mayo")
    elif num == 6:
        print("Junio")
    elif num == 7:
        print("Julio")
    elif num == 8:
        print("Agost")
    elif num == 9:
        print("Septiembre")
    elif num == 10:
        print("Octubre")
    elif num == 11:
        print("Noviembre")
    elif num == 12:
        print("Diciembre")


main()
